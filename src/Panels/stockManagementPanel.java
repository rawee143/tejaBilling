    private void remove() {

        int rowCount = stockTableModel.getRowCount();
        //Remove rows one by one from the end of the table
        for (int i = rowCount - 1; i >= 0; i--) {
            stockTableModel.removeRow(i);
        }
    }
    
    private void fillTable()
    {
       stockTableModel  =(DefaultTableModel)stockTable.getModel();
       String values = txtSearchName.getText();
        try {
            String  sql1= "SELECT * FROM product_stock where proName Like '%"+values+"%'";
            
            smtInstance= conInstance.createStatement();
            ResultSet rs1 = smtInstance.executeQuery(sql1);
                remove();
                int i = 0;
                while ( rs1.next() ) //step through the result set
                {
                    i++;//count raws
                }
                if (i>0){
                        int j= 0;
                        rs1.beforeFirst();
                        
                        while (rs1.next()) {
                    String proCode = rs1.getString("proCode");                  
                    String proName = rs1.getString("proName");
                    String qty = rs1.getString("quan");
                    String cost = rs1.getString("cost");
                    String mrp = rs1.getString("mrp");
                    stockTableModel.insertRow(j,new Object[]{proCode,proName,qty,cost,mrp});
                 
                    j++;
                }
                }
                else {
                    JOptionPane.showMessageDialog(null, "No Product Found");
                    txtSearchName.setText("");
                    txtSearchName.requestFocus();
                }
            
             
         } catch (SQLException ex) {
           // Logger.getLogger(Pharmacy_In_Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
              
}
    
    private void removeEntry(){
        try{
            String removesql = "delete FROM product_stock WHERE proCode = '"+txtProCode.getText()+"'";
            smtInstance = conInstance.createStatement();
            int result = smtInstance.executeUpdate(removesql);
            if(result!=0){
                JOptionPane.showMessageDialog(null,"Product Removed");
            }
            resetStockDetails();
            fillTable();
            
        }
        catch(SQLException ex){

        }
        txtSearchName.requestFocus();
    }
    
    private void updateEntry(){
        try{
            String updateSql = "update product_stock set quan ="+txtQuan.getText()+", proName = '"+txtProName.getText()+"', cost="+txtcost.getText()+", mrp= "+txtMRP.getText()+" where proCode = "+txtProCode.getText()+"";
            smtInstance = conInstance.createStatement();
            int result = smtInstance.executeUpdate(updateSql);
            if(result!=0){
                JOptionPane.showMessageDialog(null,"Product Updated");
                
            }
            resetStockDetails();
            fillTable();
        } catch(SQLException ex){

        } catch(HeadlessException ex){

        }
        txtSearchName.requestFocus();
    }

