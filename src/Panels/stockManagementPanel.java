    private void txtcostKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcostKeyPressed
        char car = evt.getKeyChar();
        int key = evt.getKeyCode();
        if (key == KeyEvent.VK_ENTER) {
            txtMRP.requestFocus();
        }
        else if((car < '0'|| car > '9') && txtcost.getText().contains(".") && (car!=(char)KeyEvent.VK_BACK_SPACE)&& (car!=(char)KeyEvent.VK_ENTER))
        {
            evt.consume();
            JOptionPane.showMessageDialog(null, "Invalid Insert");
            txtcost.setText("");
        }
        else if((car < '0'|| car > '9') && (car!='.') && (car!=(char)KeyEvent.VK_BACK_SPACE))
        {
            evt.consume();
            JOptionPane.showMessageDialog(null, "Invalid Insert");
            txtcost.setText("");
        }
    }//GEN-LAST:event_txtcostKeyPressed

